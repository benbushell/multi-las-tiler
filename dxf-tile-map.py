import ezdxf
import ezdxf.enums


def createTileMap(paths):

    doc = ezdxf.new("R2010")
    msp = doc.modelspace()
    
    for path in paths:
        
        i = path.rfind('/')
        fileName = path[i:]
        fileName = fileName[1:]
        
        y = int(path[-12:-4] )
        x = int(path[-23:-15] )
        
        points = [(x, y), (x, y+25), (x+25, y+25), (x+25, y)]
        msp.add_lwpolyline(points).close()

        msp.add_text(fileName, height = 0.6).set_placement(
        (x+12.5, y+12.5),
         align=ezdxf.enums.TextEntityAlignment.MIDDLE_CENTER
        )

        


    doc.saveas("dxf//test.dxf")



